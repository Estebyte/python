print("")
num = int(input("Ingresa el numero a convertir: "))

if num == 1:
    print ("El numero es 'Uno'")

elif num == 2:
    print ("El numero es 'Dos'")
elif num == 3:
    print ("El numero es 'Tres'")
elif num == 4:
    print ("El numero es 'Cuatro'")

elif num == 5:
    print ("El numero es 'Cinco'")

else:
    print ("El conversor solo acepta numeros del 1 al 5")
    
    
